.MODEL SMALL
.STACK 64
.DATA         

SAYI DB 0
SUTUN DB 00H 

CUMLE DB 'SAYAC:$' 
CHAR DB ?

.CODE
ANA PROC FAR   
  MOV AX,@DATA
  MOV DS, AX 
  
  CALL IMLECKONUM  
  LEA DX,CUMLE    
  CALL YAZ  
             
  TEKRAR:
  MOV SUTUN,8
  CALL IMLECKONUM 
  MOV AH,00
  INT 16H    
  
  CMP AH,50H
  JNZ ATLA
  DEC SAYI 
  JMP BITIR
  
  ATLA:
  
  CMP AH,48H
  JNZ BITIR
  INC SAYI  
   
  BITIR: 
  MOV  BL,SAYI 
  ADD  BL,30H
  MOV  CHAR,BL 
  CALL YAZCHAR
  JMP TEKRAR 
  
  MOV AH,4CH
  INT 21H
ANA ENDP   

IMLECKONUM PROC 
  MOV AH,02H
  MOV BH,00H
  MOV DH,00H;SATIR  
  MOV DL,SUTUN;SUTUN
  INT 10H  
  RET
IMLECKONUM ENDP   

YAZ PROC  
  MOV AH,09H
  MOV DL,BL
  INT 21H  
  RET     
YAZ ENDP  

YAZCHAR PROC
  MOV DL,CHAR          
  MOV AH,02
  INT 21H          
  RET
YAZCHAR ENDP    

ARTIR PROC
  INC SAYI,1
  RET
ARTIR ENDP

AZALT PROC
  DEC SAYI,1
  RET
AZALT ENDP

END ANA
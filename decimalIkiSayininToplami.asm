.MODEL SMALL
.STACK 64
.DATA         

  SAYI1 DB 19H
  SAYI2 DB 25H
  TOPLAM DB 0
  
  MESAJ1 DB "SAYI1: 19H$"
  MESAJ2 DB "SAYI2: 25H$"
  MESAJ3 DB "TOPLAM: --H$"
  
  SATIR DB 12
  SUTUN DB 12
  
  CHAR DB 0
  
  TABLO DB "0123456789ABCDEF"

.CODE
ANA PROC FAR   
  MOV AX,@DATA
  MOV DS, AX  
  
  CALL EKRANISIL
  CALL IMLECKONUMLA
  
  LEA DX,MESAJ1
  CALL PRINT 
  
  INC SATIR
  CALL IMLECKONUMLA
  LEA DX,MESAJ2
  CALL PRINT
  
  INC SATIR
  CALL IMLECKONUMLA
  LEA DX,MESAJ3
  CALL PRINT
  
  MOV BL,SAYI1
  ADD BL,SAYI2
  MOV TOPLAM,BL
  
  ;---------1.RAKAM
  
  MOV SATIR,14
  MOV SUTUN,21
  CALL IMLECKONUMLA
  
  MOV AL,TOPLAM
  AND AL,0FH
  
  MOV BH,0
  MOV BL,AL
  
  MOV AL,TABLO[BX]
  
  MOV CHAR,AL
  CALL YAZ 
  
  ;---------2.RAKAM
  
  MOV SATIR,14
  MOV SUTUN,20
  CALL IMLECKONUMLA
  
  MOV AL,TOPLAM
  SHR AL,4
  
  MOV BH,0
  MOV BL,AL
  
  MOV AL,TABLO[BX]
  MOV CHAR,AL
  CALL YAZ  
  
  
  MOV AH,4CH
  INT 21H
ANA ENDP   

EKRANISIL PROC
  MOV AX,0600H
  MOV BH,30H
  MOV CX,0000H
  MOV DX,184FH              
  INT 10H
  RET    
EKRANISIL ENDP

IMLECKONUMLA PROC
    MOV AH,02H
    MOV BH,00H
    MOV DH,SATIR
    MOV DL,SUTUN
    INT 10H
    RET
IMLECKONUMLA ENDP

PRINT PROC   
    MOV AH,09H
    INT 21H
    RET
PRINT ENDP  

YAZ PROC
    MOV AH,02
    MOV DL,CHAR
    INT 21H
    RET
YAZ ENDP

END ANA